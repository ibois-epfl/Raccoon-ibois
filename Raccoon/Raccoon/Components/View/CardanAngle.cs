    public static class CardanAngle
    {
        public static Mesh[] Perform_Cardan_Simulation(Point3d origin, Vector3d normal, double length, double radius, double tool_holder_length, double tool_holder_radius, bool saw_blade = false)
        {
            //Step0 Create tool
            cardan_tool tool = new cardan_tool(length, radius, tool_holder_length, tool_holder_radius, saw_blade);

            //Step1
            Tuple<double, double> result = AB180(normal);
            double a = result.Item1 + Math.PI * 0.5;
            double b = -result.Item2;

            //Step2
            double tilt_angle = Rhino.RhinoMath.ToRadians(50);
            double cEl = Math.Cos(b);
            double sH = Math.Sin(tilt_angle);
            double cH = Math.Cos(tilt_angle);
            double rotation = Math.Acos((cEl - cH * cH) / (sH * sH));

            //Step3 "Orientation with a=0"
            //Search for 4th axis angle, named a
            //Conversion of h (head angle) and b (fith axis) to radians
            Vector3d newNormal = CalculateVector(0, rotation, tilt_angle);

            //Step4 recalculate b-axis (azimuth)
            result = AB180(newNormal);
            b = result.Item1 + Math.PI * 0.5;

            //Step5 Result
            b = a - b;
            a = rotation;

            //Step6 Compute rotations
            var xforms = Tool_Simulation(length, origin.X, origin.Y, origin.Z, Rhino.RhinoMath.ToDegrees(a), Rhino.RhinoMath.ToDegrees(b));

            //Step7 Rotate Geometry
            tool.Transform(xforms);

            //Output
            return tool.GetGeometry();
        }

        // <Custom additional code>

        public static Tuple<double, double> AB180(Vector3d n0)
        {
            double a = -Math.Atan2(n0.X, n0.Y);
            double b = -Math.Atan2(Math.Sqrt(n0.X * n0.X + n0.Y * n0.Y), n0.Z);
            return new Tuple<double, double>(a, b);
        }

        /// <summary>
        ///
        /// </summary>
        /// <param name="a">a angle in radians(4th degree of freedom)</param>
        /// <param name="b">b angle in radians (5th degree of freedom)</param>
        /// <param name="h">h angle of the head</param>
        /// <returns></returns>
        public static Vector3d CalculateVector(double a, double b, double h)
        {
            double x = -Math.Cos(a) * Math.Sin(h) * Math.Cos(-h) - Math.Sin(-h) * (Math.Cos(a) * Math.Cos(h) * Math.Cos(b) - Math.Sin(a) * Math.Sin(b));
            double y = -Math.Sin(a) * Math.Sin(h) * Math.Cos(-h) - Math.Sin(-h) * (Math.Sin(a) * Math.Cos(h) * Math.Cos(b) + Math.Cos(a) * Math.Sin(b));
            double z = Math.Cos(h) * Math.Cos(-h) - Math.Sin(h) * Math.Cos(b) * Math.Sin(-h);

            return new Vector3d(x, y, z);
        }

        //Tool geometrical orientation
        public static Transform[] Tool_Simulation(
          double tool_length = 0,
          double translation_x = 0,
          double translation_y = 0,
          double translation_z = 0,
          double deg_angle_0 = 0,
          double deg_angle_1 = 0,
          double deg_angle_head_inclination = 50,
          double p0_x = 20.188139,
          double p0_y = 0,
          double p0_z = -17.021017,
          double p1_x = -9.50057,
          double p1_y = 0,
          double p1_z = -15.710087

        )
        {
            //Tool Length
            Transform xform0 = Transform.Translation(new Vector3d(0, 0, tool_length));
            Transform xform0_ = Transform.Translation(new Vector3d(0, 0, -tool_length));

            //Rotations
            Transform xform1 = Transform.Translation(new Vector3d(p0_x, p0_y, p0_z - tool_length));
            Transform xform2 = Transform.Rotation(Rhino.RhinoMath.ToRadians(deg_angle_head_inclination), Plane.WorldZX.ZAxis, Point3d.Origin);
            Transform xform3 = Transform.Rotation(Rhino.RhinoMath.ToRadians(deg_angle_0), Plane.WorldXY.ZAxis, Point3d.Origin);
            Transform xform4 = Transform.Rotation(Rhino.RhinoMath.ToRadians(-deg_angle_head_inclination), Plane.WorldZX.ZAxis, Point3d.Origin);
            Transform xform5 = Transform.Translation(new Vector3d(p1_x, p1_y, p1_z - tool_length));

            Transform xform6 = Transform.Rotation(Rhino.RhinoMath.ToRadians(deg_angle_1), Plane.WorldXY.ZAxis, Point3d.Origin);

            //Last Transform of Input Geometry
            Point3d vector_of_translation = new Point3d(0, 0, -tool_length);
            vector_of_translation.Transform(xform6 * xform5 * xform4 * xform3 * xform2 * xform1 * xform0);
            vector_of_translation *= -1;
            Vector3d overall_translation = new Vector3d(translation_x, translation_y, translation_z);
            Transform xform7 = Transform.Translation(overall_translation + (Vector3d)vector_of_translation);

            //Scale
            Transform xform8 = Transform.Scale((Point3d)overall_translation, 10);

            //First Axis
            Transform xform_merged_first_axis = xform8 * xform7 * xform6 * xform5 * xform4 * xform3 * xform2 * xform1 * xform0;

            //Second Axis
            Transform xform_merged_second_axis = xform8 * xform7 * xform6 * xform0_;

            return new Transform[] { xform_merged_first_axis, xform_merged_second_axis };
        }
    }

    public class cardan_tool
    {
        public Mesh axis_0 = new Mesh();
        public Mesh axis_0_tool = new Mesh();
        public Mesh axis_1 = new Mesh();

        public cardan_tool(double toolLength = 100, double radius = 5, double cutLength = 50, double holderRadius = 2, bool saw_blade = false)
        {
            axis_0.Vertices.AddVertices(v_axis_0);
            axis_0.Faces.AddFaces(f_axis_0);
            axis_1.Vertices.AddVertices(v_axis_1);
            axis_1.Faces.AddFaces(f_axis_1);

            if (saw_blade)
            {
                //Circular saw
                axis_0_tool = Mesh.CreateFromCylinder(new Cylinder(new Circle(new Plane(new Point3d(0, 0, -toolLength), Vector3d.ZAxis), radius), 0.5), 1, 32);
                //Holder
                axis_0_tool.Append(Mesh.CreateFromCylinder(new Cylinder(new Circle(new Plane(Point3d.Origin, -Vector3d.ZAxis), holderRadius), toolLength), 1, 32));
            }
            else
            {
                //Tool cylinder
                axis_0_tool = Mesh.CreateFromCylinder(new Cylinder(new Circle(new Plane(Point3d.Origin, -Vector3d.ZAxis), radius), toolLength), 1, 8);

                //Holder
                axis_0_tool.Append(Mesh.CreateFromCylinder(new Cylinder(new Circle(new Plane(new Point3d(0, 0, 0), -Vector3d.ZAxis), holderRadius), toolLength - cutLength), 1, 8));//
            }

            axis_0.RebuildNormals();
            axis_1.RebuildNormals();
            axis_0.Unweld(0, true);
            axis_0_tool.Unweld(0, true);
            axis_1.Unweld(0, true);
        }

        public void Transform(Transform[] xform)
        {
            axis_0.Transform(xform[0]);
            axis_0_tool.Transform(xform[0]);
            axis_1.Transform(xform[1]);
        }

        public Mesh[] GetGeometry()
        {
            return new Mesh[] { axis_0, axis_0_tool, axis_1 };
        }

